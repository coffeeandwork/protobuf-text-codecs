  private FieldDescriptorProto scalarField(
      String name, int number, FieldDescriptorProto.Type type) {
    return FieldDescriptorProto.newBuilder()
        .setName(name)
        .setNumber(number)
        .setType(type)
        .setLabel(FieldDescriptorProto.Label.LABEL_OPTIONAL)
        .build();
  }

  private FieldDescriptorProto repeatedField(
      String name, int number, FieldDescriptorProto.Type type) {
    return FieldDescriptorProto.newBuilder()
        .setName(name)
        .setNumber(number)
        .setType(type)
        .setLabel(FieldDescriptorProto.Label.LABEL_REPEATED)
        .build();
  }

  private FieldDescriptorProto messageField(String name, int number, String typeName) {
    return FieldDescriptorProto.newBuilder()
        .setName(name)
        .setNumber(number)
        .setType(FieldDescriptorProto.Type.TYPE_MESSAGE)
        .setTypeName(typeName)
        .setLabel(FieldDescriptorProto.Label.LABEL_OPTIONAL)
        .build();
  }

  private FieldDescriptorProto enumField(String name, int number, String typeName) {
    return FieldDescriptorProto.newBuilder()
        .setName(name)
        .setNumber(number)
        .setType(FieldDescriptorProto.Type.TYPE_ENUM)
        .setTypeName(typeName)
        .setLabel(FieldDescriptorProto.Label.LABEL_OPTIONAL)
        .build();
  }

  /** Generate code for a single message with the given language. */
  private String generate(DescriptorProto msg, String lang) {
    return generate(msg, lang, "test", "proto3");
  }

  private String generate(DescriptorProto msg, String lang, String pkg, String syntax) {
    FileDescriptorProto file =
        FileDescriptorProto.newBuilder()
            .setName("test.proto")
            .setPackage(pkg)
            .setSyntax(syntax)
            .addMessageType(msg)
            .build();

    CodeGeneratorRequest request =
        CodeGeneratorRequest.newBuilder()
            .addProtoFile(file)
            .addFileToGenerate("test.proto")
            .setParameter("lang=" + lang)
            .build();

    CodeGeneratorResponse response = runner.run(request);
    assertFalse(response.hasError(), lang + ": Expected no error, got: " + response.getError());
    assertTrue(response.getFileCount() > 0, lang + ": Expected at least one generated file");

    // Concatenate all generated files to cover header+source (C, C++)
    StringBuilder all = new StringBuilder();
    for (int i = 0; i < response.getFileCount(); i++) {
      all.append(response.getFile(i).getContent());
    }
    return all.toString();
  }

  private void assertContainsAny(String code, List<String> patterns, String lang, String desc) {
    for (String pattern : patterns) {
      if (code.contains(pattern)) {
        return;
      }
    }
    // Build helpful failure message
    StringBuilder sb = new StringBuilder();
    sb.append("[").append(lang).append("] ").append(desc);
    sb.append(": expected at least one of ");
    sb.append(patterns);
    sb.append(" but none found in generated code.");
    assertTrue(false, sb.toString());
  }

