  // ======================================================================

  @ParameterizedTest(name = "{0}")
  @MethodSource("languages")
  void testKeywordEscaping(LangExpectation lang) {
    // Each language has different reserved words:
    // Python/JS/TS/C++: "class" -> escaped
    // C: "struct" -> escaped with _pb suffix
    // Rust: "type" -> escaped with r# prefix
    // Go: "type" -> escaped with _ suffix
    // Zig: "const" -> escaped with @"..." syntax
    String fieldName =
        switch (lang.lang()) {
          case "c" -> "struct";
          case "rust" -> "type";
          case "go" -> "type";
          case "zig" -> "const";
          default -> "class"; // python, javascript, typescript, cpp
        };
    DescriptorProto msg =
        DescriptorProto.newBuilder()
            .setName("Msg")
            .addField(
                FieldDescriptorProto.newBuilder()
                    .setName(fieldName)
                    .setNumber(1)
                    .setType(FieldDescriptorProto.Type.TYPE_STRING)
                    .setLabel(FieldDescriptorProto.Label.LABEL_OPTIONAL)
                    .build())
            .build();
    String code = generate(msg, lang.lang());
    assertContainsAny(code, lang.patterns().get("keyword_escape"), lang.lang(), "keyword escaping");
  }

