  private static CodeGeneratorRequest buildRequest(FileDescriptorProto proto, String parameter) {
    return CodeGeneratorRequest.newBuilder()
        .addProtoFile(proto)
        .addFileToGenerate(proto.getName())
        .setParameter(parameter)
        .build();
  }
