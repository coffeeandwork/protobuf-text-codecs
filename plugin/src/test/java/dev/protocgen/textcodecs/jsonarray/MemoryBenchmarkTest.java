  // ======================================================================
  // 3. Comparison: JSON array vs pbtk URL memory
  // ======================================================================

  @Test
  void compareJsonArrayVsPbtkUrlMemory() {
    PluginRunner jsonRunner = new PluginRunner();
    PbtkPluginRunner pbtkRunner = new PbtkPluginRunner();

    // Warm up both
    for (int i = 0; i < 5; i++) {
      jsonRunner.run(jsonArrayJavaRequest);
      pbtkRunner.run(pbtkUrlJavaRequest);
    }

    long[] jsonAllocated = new long[MEASUREMENT_ITERATIONS];
    long[] pbtkAllocated = new long[MEASUREMENT_ITERATIONS];

    for (int i = 0; i < MEASUREMENT_ITERATIONS; i++) {
      forceGc();
      long before = getUsedHeap();
      jsonRunner.run(jsonArrayJavaRequest);
      jsonAllocated[i] = getUsedHeap() - before;

      forceGc();
      before = getUsedHeap();
      pbtkRunner.run(pbtkUrlJavaRequest);
      pbtkAllocated[i] = getUsedHeap() - before;
    }

    System.err.println();
    System.err.println("=== Memory Comparison: JSON Array vs pbtk URL ===");
    reportMemoryStats("JSON array allocated", jsonAllocated);
    reportMemoryStats("pbtk URL allocated", pbtkAllocated);

    long jsonMedian = median(jsonAllocated);
    long pbtkMedian = median(pbtkAllocated);
    if (pbtkMedian > 0 && jsonMedian > 0) {
      double ratio = (double) pbtkMedian / jsonMedian;
      System.err.printf("  pbtk/jsonarray ratio: %.2fx%n", ratio);
    }
    System.err.println();

    // Both should be reasonable
    assertTrue(median(jsonAllocated) < 10_000_000L, "JSON array generation memory too high");
    assertTrue(median(pbtkAllocated) < 10_000_000L, "pbtk URL generation memory too high");
  }

  // ======================================================================
  // 4. Model object memory -- ProtoField, ProtoMessage object sizes
  // ======================================================================

  @Test
  void measureModelObjectMemory() {
    dev.protocgen.textcodecs.jsonarray.model.TypeRegistry registry =
        new dev.protocgen.textcodecs.jsonarray.model.TypeRegistry();
    registry.registerFile(complexProto);
    MessageAnalyzer analyzer = new MessageAnalyzer(registry);

    // Warm up
    for (int i = 0; i < 5; i++) {
      analyzer.analyze(complexProto.getMessageType(0), ".bench.");
    }

    long[] analyzerAllocated = new long[MEASUREMENT_ITERATIONS];

    for (int i = 0; i < MEASUREMENT_ITERATIONS; i++) {
      forceGc();
      long before = getUsedHeap();
      var msg = analyzer.analyze(complexProto.getMessageType(0), ".bench.");
      analyzerAllocated[i] = getUsedHeap() - before;
      // Prevent dead-code elimination
      if (msg.getFields().isEmpty()) throw new AssertionError("no fields");
    }

    System.err.println();
    System.err.println("=== Model Object Memory (MessageAnalyzer.analyze) ===");
    reportMemoryStats("Memory per analyze() call", analyzerAllocated);

    // Estimate per-field overhead
    long medianAlloc = median(analyzerAllocated);
    int fieldCount = 16; // KitchenSink has 16 fields
    long perFieldEstimate = medianAlloc / fieldCount;
    System.err.printf("  Fields in KitchenSink: %d%n", fieldCount);
    System.err.printf("  Estimated bytes per field: ~%,d bytes%n", perFieldEstimate);
    System.err.printf(
        "    (includes ProtoField object + Builder + String allocations + list overhead)%n");
    System.err.println();

    // ProtoField should be under 32KB per field including all transient objects
    // (heap-diff measurements have significant noise due to GC timing)
    assertTrue(perFieldEstimate < 32_768,
        "Per-field memory too high: " + perFieldEstimate + " bytes");
  }

  // ======================================================================
  // Utility methods
  // ======================================================================

  /** Force garbage collection with stabilization. */
  private static void forceGc() {
    for (int i = 0; i < GC_SETTLE_ATTEMPTS; i++) {
      System.gc();
      try {
        Thread.sleep(10);
      } catch (InterruptedException e) {
        Thread.currentThread().interrupt();
        break;
      }
    }
  }

  /** Get current used heap memory. */
  private static long getUsedHeap() {
    Runtime rt = Runtime.getRuntime();
    return rt.totalMemory() - rt.freeMemory();
  }

  /** Compute median of a long array. */
  private static long median(long[] values) {
    long[] sorted = values.clone();
    java.util.Arrays.sort(sorted);
    int mid = sorted.length / 2;
    if (sorted.length % 2 == 0) {
      return (sorted[mid - 1] + sorted[mid]) / 2;
    }
    return sorted[mid];
  }

  /** Report min/median/max/mean for a set of memory measurements. */
  private static void reportMemoryStats(String label, long[] values) {
    long[] sorted = values.clone();
    java.util.Arrays.sort(sorted);
    long min = sorted[0];
    long max = sorted[sorted.length - 1];
    long med = sorted[sorted.length / 2];
    double mean = 0;
    for (long v : sorted) mean += v;
    mean /= sorted.length;
    System.err.printf("  %-40s  min=%,12d  median=%,12d  max=%,12d  mean=%,.0f bytes%n",
        label, min, med, max, mean);
  }

