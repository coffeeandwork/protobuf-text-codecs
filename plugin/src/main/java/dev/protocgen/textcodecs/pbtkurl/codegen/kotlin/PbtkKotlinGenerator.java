  private void emitToString(CodeWriter w, ProtoMessage message, String className) {
    w.blankLine();
    w.block(
        "override fun toString(): String",
        () -> {
          if (message.getFields().isEmpty()) {
            w.line("return \"%s{}\"", className);
            return;
          }
          w.line("return \"%s{\" +", className);
          w.indent();
          for (int i = 0; i < message.getFields().size(); i++) {
            ProtoField field = message.getFields().get(i);
            String ktName = nameResolver.fieldName(field.getName());
            String prefix = i == 0 ? "\"" : "\", ";
            boolean isLast = i == message.getFields().size() - 1;
            if (isLast) {
              w.line("%s%s=\" + %s + \"}\"", prefix, field.getName(), ktName);
            } else {
              w.line("%s%s=\" + %s +", prefix, field.getName(), ktName);
            }
          }
          w.dedent();
        });
  }

  private void emitEquals(CodeWriter w, ProtoMessage message, String className) {
    w.blankLine();
    w.block(
        "override fun equals(other: Any?): Boolean",
        () -> {
          w.line("if (this === other) return true");
          w.line("if (other !is %s) return false", className);
          if (message.getFields().isEmpty()) {
            w.line("return true");
            return;
          }
          StringBuilder sb = new StringBuilder("return ");
          for (int i = 0; i < message.getFields().size(); i++) {
            ProtoField field = message.getFields().get(i);
            String ktName = nameResolver.fieldName(field.getName());
            if (i > 0) sb.append(" && ");
            if (isByteArrayField(field)) {
              sb.append(String.format("this.%s.contentEquals(other.%s)", ktName, ktName));
            } else if (isFloatField(field)) {
              sb.append(String.format("this.%s.compareTo(other.%s) == 0", ktName, ktName));
            } else if (isDoubleField(field)) {
              sb.append(String.format("this.%s.compareTo(other.%s) == 0", ktName, ktName));
            } else {
              sb.append(String.format("this.%s == other.%s", ktName, ktName));
            }
          }
          w.line(sb.toString());
        });
  }

  private void emitHashCode(CodeWriter w, ProtoMessage message) {
    w.blankLine();
    w.block(
        "override fun hashCode(): Int",
        () -> {
          if (message.getFields().isEmpty()) {
            w.line("return 0");
            return;
          }
          w.line("var result = 0");
          for (ProtoField field : message.getFields()) {
            String ktName = nameResolver.fieldName(field.getName());
            if (isByteArrayField(field)) {
              w.line("result = 31 * result + %s.contentHashCode()", ktName);
            } else if (isDoubleField(field)) {
              w.line("result = 31 * result + %s.toBits().hashCode()", ktName);
            } else if (isFloatField(field)) {
              w.line("result = 31 * result + %s.toBits().hashCode()", ktName);
            } else {
              w.line("result = 31 * result + %s.hashCode()", ktName);
            }
          }
          w.line("return result");
        });
  }

