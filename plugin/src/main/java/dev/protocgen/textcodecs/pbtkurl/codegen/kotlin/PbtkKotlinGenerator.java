  private void emitBuilder(CodeWriter w, ProtoMessage message, String className) {
    w.blankLine();
    w.block(
        "class Builder",
        () -> {
          for (ProtoField field : message.getFields()) {
            String ktType = typeMapper.languageType(field);
            String ktName = nameResolver.fieldName(field.getName());
            String defaultVal = typeMapper.defaultValue(field);
            w.line("internal var %s: %s = %s", ktName, ktType, defaultVal);
          }

          if (hasPresenceTrackedFields(message)) {
            w.blankLine();
            w.line("internal val presentFields_ = java.util.BitSet()");
          }

          for (ProtoMessage.OneofGroup group : message.getOneofGroups()) {
            w.blankLine();
            String caseName = nameResolver.fieldName(group.name()) + "Case_";
            w.line("internal var %s: Int = 0", caseName);
          }

          // Setters
          for (ProtoField field : message.getFields()) {
            String ktType = typeMapper.languageType(field);
            String ktName = nameResolver.fieldName(field.getName());
            String pascalName = KotlinNameResolver.snakeToPascal(field.getName());

            // set
            w.blankLine();
            w.block(
                "fun set" + pascalName + "(value: " + ktType + "): Builder",
                () -> {
                  w.line("this.%s = value", ktName);
                  if (field.hasExplicitPresence()) {
                    w.line("this.presentFields_.set(%d)", field.getArrayPosition());
                  }
                  if (field.isOneofMember()) {
                    w.line(
                        "this.%sCase_ = %sCase_",
                        nameResolver.fieldName(field.getOneofName()),
                        field.getName().toUpperCase());
                  }
                  w.line("return this");
                });

            // clear
            w.blankLine();
            String defaultVal = typeMapper.defaultValue(field);
            w.block(
                "fun clear" + pascalName + "(): Builder",
                () -> {
                  w.line("this.%s = %s", ktName, defaultVal);
                  if (field.hasExplicitPresence()) {
                    w.line("this.presentFields_.clear(%d)", field.getArrayPosition());
                  }
                  if (field.isOneofMember()) {
                    String caseName = nameResolver.fieldName(field.getOneofName()) + "Case_";
                    w.block(
                        "if (this." + caseName + " == " + field.getName().toUpperCase() + "Case_)",
                        () -> w.line("this.%s = 0", caseName));
                  }
                  w.line("return this");
                });

            // repeated: add / addAll
            if (field.isRepeated()) {
              String elemType = elementType(field);
              w.blankLine();
              w.block(
                  "fun add" + pascalName + "(value: " + elemType + "): Builder",
                  () -> {
                    w.line("this.%s.add(value)", ktName);
                    w.line("return this");
                  });
              w.blankLine();
              w.block(
                  "fun addAll" + pascalName + "(values: Iterable<" + elemType + ">): Builder",
                  () -> {
                    w.block("for (v in values)", () -> w.line("this.%s.add(v)", ktName));
                    w.line("return this");
                  });
            }

            // map: put / putAll / remove
            if (field.isMap()) {
              String keyType = typeMapper.scalarType(field.getMapKeyType());
              String valueType = mapValueType(field);
              w.blankLine();
              w.block(
                  "fun put"
                      + pascalName
                      + "(key: "
                      + keyType
                      + ", value: "
                      + valueType
                      + "): Builder",
                  () -> {
                    w.line("this.%s[key] = value", ktName);
                    w.line("return this");
                  });
              w.blankLine();
              w.block(
                  "fun putAll"
                      + pascalName
                      + "(values: Map<"
                      + keyType
                      + ", "
                      + valueType
                      + ">): Builder",
                  () -> {
                    w.line("this.%s.putAll(values)", ktName);
                    w.line("return this");
                  });
              w.blankLine();
              w.block(
                  "fun remove" + pascalName + "(key: " + keyType + "): Builder",
                  () -> {
                    w.line("this.%s.remove(key)", ktName);
                    w.line("return this");
                  });
            }
          }

          // clear()
          w.blankLine();
          w.block(
              "fun clear(): Builder",
              () -> {
                for (ProtoField field : message.getFields()) {
                  String ktName = nameResolver.fieldName(field.getName());
                  String defaultVal = typeMapper.defaultValue(field);
                  w.line("this.%s = %s", ktName, defaultVal);
                }
                if (hasPresenceTrackedFields(message)) {
                  w.line("this.presentFields_.clear()");
                }
                for (ProtoMessage.OneofGroup group : message.getOneofGroups()) {
                  String caseName = nameResolver.fieldName(group.name()) + "Case_";
                  w.line("this.%s = 0", caseName);
                }
                w.line("return this");
              });

          // build()
          w.blankLine();
          w.block("fun build(): " + className, () -> w.line("return %s(this)", className));
        });
  }

  // ---------------------------------------------------------------------------
  // Companion object
  // ---------------------------------------------------------------------------

  private void emitCompanionObject(CodeWriter w, ProtoMessage message, String className) {
    w.blankLine();
    w.block(
        "companion object",
        () -> {
          // Field number constants
          for (ProtoField field : message.getFields()) {
            String constName = snakeToUpperSnake(field.getName()) + "_FIELD_NUMBER";
            w.line("const val %s: Int = %d", constName, field.getFieldNumber());
          }

          // Oneof case constants
          for (ProtoMessage.OneofGroup group : message.getOneofGroups()) {
            for (ProtoField member : group.members()) {
              w.line(
                  "private const val %sCase_: Int = %d",
                  member.getName().toUpperCase(), member.getFieldNumber());
            }
          }

          // DEFAULT_INSTANCE
          w.blankLine();
          w.line("private val DEFAULT_INSTANCE: %s = Builder().build()", className);

          // getDefaultInstance()
          w.blankLine();
          w.line("@JvmStatic");
          w.block(
              "fun getDefaultInstance(): " + className, () -> w.line("return DEFAULT_INSTANCE"));

          // newBuilder()
          w.blankLine();
          w.line("@JvmStatic");
          w.block("fun newBuilder(): Builder", () -> w.line("return Builder()"));

          // newBuilder(prototype)
          w.blankLine();
          w.line("@JvmStatic");
          w.block(
              "fun newBuilder(prototype: " + className + "): Builder",
              () -> {
                w.line("val builder = Builder()");
                for (ProtoField field : message.getFields()) {
                  String ktName = nameResolver.fieldName(field.getName());
                  if (field.isRepeated()) {
                    w.line("builder.%s.addAll(prototype.%s)", ktName, ktName);
                  } else if (field.isMap()) {
                    w.line("builder.%s.putAll(prototype.%s)", ktName, ktName);
                  } else {
                    w.line("builder.%s = prototype.%s", ktName, ktName);
                  }
                }
                if (hasPresenceTrackedFields(message)) {
                  w.line("builder.presentFields_.or(prototype.presentFields_)");
                }
                for (ProtoMessage.OneofGroup group : message.getOneofGroups()) {
                  String caseName = nameResolver.fieldName(group.name()) + "Case_";
                  w.line("builder.%s = prototype.%s", caseName, caseName);
                }
                w.line("return builder");
              });

          // Deserializer
          emitDeserializer(w, message, className);
        });
  }

