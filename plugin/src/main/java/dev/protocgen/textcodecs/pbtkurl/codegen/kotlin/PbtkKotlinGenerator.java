  private void emitDocComment(CodeWriter w, String comment) {
    if (comment == null || comment.isEmpty()) return;
    String safeComment = comment.replace("*/", "* /");
    String[] lines = safeComment.split("\n");
    if (lines.length == 1) {
      w.line("/** %s */", lines[0].strip());
    } else {
      w.line("/**");
      for (String line : lines) {
        String trimmed = line.strip();
        if (trimmed.isEmpty()) {
          w.line(" *");
        } else {
          w.line(" * %s", trimmed);
        }
      }
      w.line(" */");
    }
  }

  private String elementType(ProtoField field) {
    if (field.getKind() == ProtoField.FieldKind.MESSAGE
        || field.getKind() == ProtoField.FieldKind.WELL_KNOWN_TYPE) {
      return simpleTypeName(field.getTypeReference());
    }
    if (field.getKind() == ProtoField.FieldKind.ENUM) {
      return simpleTypeName(field.getTypeReference());
    }
    return typeMapper.scalarType(field.getProtoType());
  }

  private String mapValueType(ProtoField field) {
    if (field.getMapValueType() == FieldDescriptorProto.Type.TYPE_MESSAGE
        || field.getMapValueType() == FieldDescriptorProto.Type.TYPE_ENUM) {
      return simpleTypeName(field.getMapValueTypeReference());
    }
    return typeMapper.scalarType(field.getMapValueType());
  }

  private String simpleTypeName(String protoFullName) {
    if (protoFullName == null) return "Any";
    int lastDot = protoFullName.lastIndexOf('.');
    return lastDot >= 0 ? protoFullName.substring(lastDot + 1) : protoFullName;
  }

  private static String snakeToUpperSnake(String name) {
    StringBuilder sb = new StringBuilder();
    for (int i = 0; i < name.length(); i++) {
      char c = name.charAt(i);
      if (Character.isUpperCase(c) && i > 0 && name.charAt(i - 1) != '_') {
        sb.append('_');
      }
      sb.append(Character.toUpperCase(c));
    }
    return sb.toString();
  }

  static String pbtkTypeChar(FieldDescriptorProto.Type type) {
    return switch (type) {
      case TYPE_BOOL -> "b";
      case TYPE_INT32,
          TYPE_SINT32,
          TYPE_SFIXED32,
          TYPE_UINT32,
          TYPE_FIXED32,
          TYPE_INT64,
          TYPE_SINT64,
          TYPE_SFIXED64,
          TYPE_UINT64,
          TYPE_FIXED64 ->
          "i";
      case TYPE_FLOAT -> "f";
      case TYPE_DOUBLE -> "d";
      case TYPE_STRING -> "s";
      case TYPE_ENUM -> "e";
      case TYPE_MESSAGE -> "m";
      case TYPE_BYTES -> "z";
      default -> "s";
    };
  }

  private boolean isByteArrayField(ProtoField field) {
    return field.getProtoType() == FieldDescriptorProto.Type.TYPE_BYTES && field.isSingular();
  }

  private boolean isFloatField(ProtoField field) {
    return field.isSingular()
        && field.getKind() == ProtoField.FieldKind.SCALAR
        && field.getProtoType() == FieldDescriptorProto.Type.TYPE_FLOAT;
  }

  private boolean isDoubleField(ProtoField field) {
    return field.isSingular()
        && field.getKind() == ProtoField.FieldKind.SCALAR
        && field.getProtoType() == FieldDescriptorProto.Type.TYPE_DOUBLE;
  }

  private boolean hasPresenceTrackedFields(ProtoMessage message) {
    return message.getFields().stream().anyMatch(ProtoField::hasExplicitPresence);
  }
