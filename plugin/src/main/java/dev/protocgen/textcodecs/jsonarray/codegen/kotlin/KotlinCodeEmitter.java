  // Enum generation
  // ---------------------------------------------------------------------------

  private void emitEnum(CodeWriter w, ProtoEnum protoEnum) {
    w.blankLine();
    w.block(
        "enum class " + protoEnum.getName() + "(val number: Int)",
        () -> {
          for (int i = 0; i < protoEnum.getValues().size(); i++) {
            ProtoEnum.EnumValue val = protoEnum.getValues().get(i);
            String suffix = i < protoEnum.getValues().size() - 1 ? "," : ";";
            w.line("%s(%d)%s", val.name(), val.number(), suffix);
          }

          w.blankLine();
          w.block(
              "fun getNumber(): Int",
              () -> {
                w.line("return number");
              });

          w.blankLine();
          w.block(
              "companion object",
              () -> {
                w.line("@JvmStatic");
                w.block(
                    "fun forNumber(number: Int): " + protoEnum.getName() + "?",
                    () -> {
                      w.block(
                          "for (v in entries)",
                          () -> {
                            w.block(
                                "if (v.number == number)",
                                () -> {
                                  w.line("return v");
                                });
                          });
                      w.line("return null");
                    });
              });
        });
  }

